#include <stdio.h>

int main() {
     int a ;
     scanf("%d", &a);
     int b;    
     scanf("%d", &b);

     printf ("Sum is %d\n", a + b);
     printf ("Difference is %d\n", a - b);   
     printf ("Product is %d\n", a * b);
     printf ("Quotient is %d\n", a / b);
     printf ("Remainder is %d\n", a % b);
      
     printf("Maths using operators");


    
     return 0;
}