#include <stdio.h>

int main()   {
   
   
   // To print a variable, use printf function
    //  TEMPLATE: printf("%d", variable_name); for integers
    //  TEMPLATE: printf("%f", variable_name); for floats
    //  TEMPLATE: printf("%c", variable_name); for characters
    //  TEMPLATE: printf("%s", variable_name); for strings
    
    int a= 10;
    float b= 102.3;
    char c= 'A';

    printf("int %d\n", a); // prints the integer variable a
    printf("float %f\n", b); // prints the float variable b
    printf("char %c\n", c); // prints the character variable c


    return 0;

 }